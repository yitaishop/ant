cc.Class({
    extends: cc.Component,

    properties: {
        msgLabel:cc.Label,
    },

    start () {
        this.initUserInfoButton();
    },

    initUserInfoButton () {
        if (typeof wx === 'undefined') {
            return;
        }
        let systemInfo = wx.getSystemInfoSync();
        let width = systemInfo.windowWidth;
        let height = systemInfo.windowHeight;
        let button = wx.createUserInfoButton({
            type: 'text',
            text: '',
            style: {
                left: 0,
                top: 0,
                width: width,
                height: height,
                lineHeight: 40,
                backgroundColor: '#00000000',
                color: '#00000000',
                textAlign: 'center',
                fontSize: 10,
                borderRadius: 4
            }
        });

        button.onTap((res) => {
            let userInfo = res.userInfo;
            if (!userInfo) {
                this.msgLabel.string = res.errMsg;
                return;
            }
            wx.getOpenDataContext().postMessage({
                message: "success"
            });
            button.hide();
            button.destroy();

        });
    },

    onClose(){
        this.node.destroy();
    }


    // update (dt) {},
});
